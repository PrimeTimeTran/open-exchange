import { Feedback } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  FeedbackWithRelationships,
  feedbackAutocompleteInputSchema,
  feedbackCreateInputSchema,
  feedbackFindManyInputSchema,
  feedbackImportInputSchema,
  feedbackUpdateBodyInputSchema,
} from 'src/features/feedback/feedbackSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function feedbackAutocompleteApiCall(
  query?: z.input<typeof feedbackAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/feedback/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as Feedback[];
}

