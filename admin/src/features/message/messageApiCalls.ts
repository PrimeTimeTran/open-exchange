import { Message } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  MessageWithRelationships,
  messageAutocompleteInputSchema,
  messageCreateInputSchema,
  messageFindManyInputSchema,
  messageImportInputSchema,
  messageUpdateBodyInputSchema,
} from 'src/features/message/messageSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function messageAutocompleteApiCall(
  query?: z.input<typeof messageAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/message/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as Message[];
}

