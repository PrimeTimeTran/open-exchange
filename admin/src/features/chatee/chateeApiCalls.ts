import { Chatee } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  ChateeWithRelationships,
  chateeAutocompleteInputSchema,
  chateeCreateInputSchema,
  chateeFindManyInputSchema,
  chateeImportInputSchema,
  chateeUpdateBodyInputSchema,
} from 'src/features/chatee/chateeSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function chateeAutocompleteApiCall(
  query?: z.input<typeof chateeAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/chatee/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as Chatee[];
}

