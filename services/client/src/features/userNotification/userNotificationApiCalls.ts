import { UserNotification } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  UserNotificationWithRelationships,
  userNotificationAutocompleteInputSchema,
  userNotificationCreateInputSchema,
  userNotificationFindManyInputSchema,
  userNotificationImportInputSchema,
  userNotificationUpdateBodyInputSchema,
} from 'src/features/userNotification/userNotificationSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function userNotificationAutocompleteApiCall(
  query?: z.input<typeof userNotificationAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/user-notification/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as UserNotification[];
}

