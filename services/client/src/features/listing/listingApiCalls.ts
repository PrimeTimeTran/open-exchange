import { Listing } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  ListingWithRelationships,
  listingAutocompleteInputSchema,
  listingCreateInputSchema,
  listingFindManyInputSchema,
  listingImportInputSchema,
  listingUpdateBodyInputSchema,
} from 'src/features/listing/listingSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function listingAutocompleteApiCall(
  query?: z.input<typeof listingAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/listing/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as Listing[];
}

