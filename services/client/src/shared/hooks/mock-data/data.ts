const generateStrikes = () => {
  const strikes: {
    strike: number;
    isCurrent: boolean;
    call?: {
      last: number;
      change: number;
      volume: number;
      bid: number;
      ask: number;
    };
    put?: {
      last: number;
      change: number;
      volume: number;
      bid: number;
      ask: number;
    };
  }[] = [];
  const startStrike = 330;
  const currentPrice = 136.93; // For reference

  // Generate 20 rows of strikes
  for (let i = 0; i < 20; i++) {
    const strike = startStrike - i * 5;
    const isCurrent = strike === 275; // Arbitrary current/ATM strike

    strikes.push({
      strike,
      isCurrent,
      call: {
        last: Math.random() * 0.1,
        change: 0.0,
        volume: 0,
        bid: Math.random() * 0.1,
        ask: Math.random() * 0.1 + 0.05,
      },
      put: {
        last: Math.random() * 200, // Deep ITM puts expensive
        change: -0.98,
        volume: 0,
        bid: 190 + Math.random() * 5,
        ask: 192 + Math.random() * 5,
      },
    });
  }
  return strikes;
};

