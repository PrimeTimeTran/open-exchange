import { Order } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  OrderWithRelationships,
  orderAutocompleteInputSchema,
  orderCreateInputSchema,
  orderFindManyInputSchema,
  orderImportInputSchema,
  orderUpdateBodyInputSchema,
} from 'src/features/order/orderSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function orderAutocompleteApiCall(
  query?: z.input<typeof orderAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/order/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as Order[];
}

