import { FeeSchedule } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  FeeScheduleWithRelationships,
  feeScheduleAutocompleteInputSchema,
  feeScheduleCreateInputSchema,
  feeScheduleFindManyInputSchema,
  feeScheduleImportInputSchema,
  feeScheduleUpdateBodyInputSchema,
} from 'src/features/feeSchedule/feeScheduleSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function feeScheduleAutocompleteApiCall(
  query?: z.input<typeof feeScheduleAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/fee-schedule/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as FeeSchedule[];
}

