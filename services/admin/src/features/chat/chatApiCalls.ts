import { Chat } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  ChatWithRelationships,
  chatAutocompleteInputSchema,
  chatCreateInputSchema,
  chatFindManyInputSchema,
  chatImportInputSchema,
  chatUpdateBodyInputSchema,
} from 'src/features/chat/chatSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function chatAutocompleteApiCall(
  query?: z.input<typeof chatAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/chat/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as Chat[];
}

