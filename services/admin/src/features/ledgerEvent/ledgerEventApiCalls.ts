import { LedgerEvent } from '@prisma/client';
import { objectToQuery } from 'src/shared/lib/objectToQuery';
import {
  LedgerEventWithRelationships,
  ledgerEventAutocompleteInputSchema,
  ledgerEventCreateInputSchema,
  ledgerEventFindManyInputSchema,
  ledgerEventImportInputSchema,
  ledgerEventUpdateBodyInputSchema,
} from 'src/features/ledgerEvent/ledgerEventSchemas';
import { ApiErrorPayload } from 'src/shared/errors/ApiErrorPayload';
import { importerOutputSchema } from 'src/shared/schemas/importerSchemas';
import { z } from 'zod';

export async function ledgerEventAutocompleteApiCall(
  query?: z.input<typeof ledgerEventAutocompleteInputSchema>,
  signal?: AbortSignal,
) {
  const response = await fetch(
    `${
      process.env.NEXT_PUBLIC_BACKEND_URL || ''
    }/api/ledger-event/autocomplete?${objectToQuery(query)}`,
    {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      signal,
    },
  );

  if (!response.ok) {
    const payload = (await response.json()) as ApiErrorPayload;
    throw new Error(payload.errors?.[0]?.message);
  }

  return (await response.json()) as LedgerEvent[];
}

